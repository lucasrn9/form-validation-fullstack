const mongoose = require('mongoose');

const registerModel = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        minlength: 2,
        maxlength: 255
    },
    email: {
        type: String,
        required: true,
        minlength: 3,
        maxlength: 255
    },
    password: {
        type: String,
        required: true,
        minlength: 6,
        maxlength: 1024
    },
    date: {
        type: Date,
        default: Date.now()
    },
})

module.exports = mongoose.model('users',registerModel)